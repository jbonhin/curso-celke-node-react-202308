var valorUm = 17;
var valorDois = 3;

var totalAdicao = valorUm + valorDois;
document.write("<h2>Valor da Adição: " + totalAdicao + "</h2>");

var totalSubtracao = valorUm - valorDois;
document.write("<h2>Valor da Subtração: " + totalSubtracao + "</h2>");

var totalMutiplicacao = valorUm * valorDois;
document.write("<h2>Valor da Multiplicação: " + totalMutiplicacao + "</h2>");

var totalDivisao = valorUm / valorDois;
document.write("<h2>Valor da Divisão: " + totalDivisao + "</h2>");


var totalResto = valorUm % valorDois;
document.write("<h2>Valor do Resto: " + totalResto + "</h2>");